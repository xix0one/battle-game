
def print_help():
    print()
    print('\tcommands' + '*' * 30)
    print('\t| go: "g"; battle: "b"; try run: "r" |')
    print(('\t| switch char: "s";').ljust(38, ' ') + '|')
    print('\t' + '*' * 38)
    print()

def switch_help():
    print()
    print('\tcommands' + '*' * 36)
    print('\t| arrow down: "s"; arrow up: "w"; use: "e" |')
    print(('\t| choose char for switch ').ljust(44, ' ') + '|')
    print('\t' + '*' * 44)
    print()
